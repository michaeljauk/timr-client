#!/usr/bin/env node
// Reads openapi.json and emits one TypeScript file per resource under
// packages/cli/src/commands/generated/, plus an index barrel.
//
// Command naming:
//   GET    /x            -> list
//   POST   /x            -> create
//   GET    /x/{id}       -> get
//   PATCH  /x/{id}       -> update
//   DELETE /x/{id}       -> delete
//   GET    /x:deleted    -> list-deleted
//   GET    /x/{id}/sub             -> list-sub | get-sub
//   POST   /x/{id}/sub             -> create-sub
//   PUT    /x/{id}/sub             -> replace-sub
//   DELETE /x/{id}/sub             -> delete-sub
//   GET    /x/{id}/sub/{sid}       -> get-<singular(sub)>
//   PUT    /x/{id}/sub/{sid}       -> add-<singular(sub)>
//   PATCH  /x/{id}/sub/{sid}       -> update-<singular(sub)>
//   DELETE /x/{id}/sub/{sid}       -> remove-<singular(sub)>
import { readFileSync, writeFileSync, mkdirSync, rmSync, existsSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(__dirname, "..", "..", "..");
const specPath = join(repoRoot, "openapi.json");
const outDir = resolve(__dirname, "..", "src", "commands", "generated");

const spec = JSON.parse(readFileSync(specPath, "utf8"));

if (existsSync(outDir)) rmSync(outDir, { recursive: true, force: true });
mkdirSync(outDir, { recursive: true });

const resources = new Map();

for (const [path, methods] of Object.entries(spec.paths)) {
  for (const [method, op] of Object.entries(methods)) {
    if (!["get", "post", "put", "patch", "delete"].includes(method)) continue;
    const ep = parseEndpoint(method.toUpperCase(), path, op);
    if (!ep) continue;
    if (!resources.has(ep.resource)) resources.set(ep.resource, []);
    resources.get(ep.resource).push(ep);
  }
}

const exported = [];
for (const [resource, endpoints] of [...resources.entries()].sort()) {
  const file = emitResourceFile(resource, endpoints);
  const name = `${toCamel(resource)}Command`;
  const filename = `${resource}.ts`;
  writeFileSync(join(outDir, filename), file, "utf8");
  exported.push({ name, filename: `./${resource}.js`, resource });
}

const indexFile =
  "// AUTO-GENERATED by scripts/generate-commands.mjs - DO NOT EDIT\n" +
  exported.map((e) => `export { ${e.name} } from "${e.filename}";`).join("\n") +
  "\n";
writeFileSync(join(outDir, "index.ts"), indexFile, "utf8");

const rootFile =
  "// AUTO-GENERATED by scripts/generate-commands.mjs - DO NOT EDIT\n" +
  `import type { CommandDef } from "citty";\n` +
  exported.map((e) => `import { ${e.name} } from "${e.filename}";`).join("\n") +
  `\n\nexport const generatedSubCommands: Record<string, CommandDef> = {\n` +
  exported.map((e) => `  "${e.resource}": ${e.name},`).join("\n") +
  "\n};\n";
writeFileSync(join(outDir, "_root.ts"), rootFile, "utf8");

console.log(
  `generated ${exported.length} resource command files (${[...resources.values()].reduce((a, xs) => a + xs.length, 0)} endpoints)`,
);

// ---------- parsing ----------

function parseEndpoint(method, path, op) {
  const rawSegs = path.split("/").filter(Boolean);
  const first = rawSegs[0];
  const isDeletedList = first.endsWith(":deleted");
  const resource = isDeletedList ? first.slice(0, -":deleted".length) : first;
  const restSegs = rawSegs.slice(1);

  if (isDeletedList && method === "GET") {
    return {
      resource,
      command: "list-deleted",
      method,
      path,
      description: cleanSummary(op.summary, "List soft-deleted " + resource),
      positionals: [],
      query: op.parameters ?? [],
      hasBody: !!op.requestBody,
    };
  }

  if (restSegs.length === 0) {
    if (method === "GET")
      return mkEp(resource, "list", op, method, path, [], op.parameters ?? [], false, `List ${resource}`);
    if (method === "POST")
      return mkEp(resource, "create", op, method, path, [], [], true, `Create a ${singular(resource)}`);
  }

  if (restSegs.length === 1 && isParam(restSegs[0])) {
    const pid = paramName(restSegs[0]);
    const pos = [{ name: pid, description: `${singular(resource)} ${pid}` }];
    if (method === "GET")
      return mkEp(resource, "get", op, method, path, pos, [], false, `Get a ${singular(resource)} by id`);
    if (method === "PATCH")
      return mkEp(resource, "update", op, method, path, pos, [], true, `Update a ${singular(resource)}`);
    if (method === "PUT")
      return mkEp(resource, "replace", op, method, path, pos, [], true, `Replace a ${singular(resource)}`);
    if (method === "DELETE")
      return mkEp(resource, "delete", op, method, path, pos, [], false, `Delete a ${singular(resource)}`);
  }

  if (restSegs.length === 2 && isParam(restSegs[0]) && !isParam(restSegs[1])) {
    const pid = paramName(restSegs[0]);
    const sub = restSegs[1];
    const pos = [{ name: pid, description: `${singular(resource)} ${pid}` }];
    const desc = (verb) => `${verb} ${sub} of a ${singular(resource)}`;
    if (method === "GET")
      return mkEp(resource, `list-${sub}`, op, method, path, pos, op.parameters ?? [], false, desc("List"));
    if (method === "POST")
      return mkEp(resource, `create-${sub}`, op, method, path, pos, [], true, desc("Create"));
    if (method === "PUT")
      return mkEp(resource, `replace-${sub}`, op, method, path, pos, [], true, desc("Replace"));
    if (method === "PATCH")
      return mkEp(resource, `update-${sub}`, op, method, path, pos, [], true, desc("Update"));
    if (method === "DELETE")
      return mkEp(resource, `delete-${sub}`, op, method, path, pos, [], false, desc("Delete"));
  }

  if (
    restSegs.length === 3 &&
    isParam(restSegs[0]) &&
    !isParam(restSegs[1]) &&
    isParam(restSegs[2])
  ) {
    const p1 = paramName(restSegs[0]);
    const sub = restSegs[1];
    const p2 = paramName(restSegs[2]);
    const pos = [
      { name: p1, description: `${singular(resource)} ${p1}` },
      { name: p2, description: `${singular(sub)} ${p2}` },
    ];
    const s = singular(sub);
    if (method === "GET")
      return mkEp(resource, `get-${s}`, op, method, path, pos, [], false, `Get a ${s} of a ${singular(resource)}`);
    if (method === "PUT")
      return mkEp(resource, `add-${s}`, op, method, path, pos, [], false, `Add a ${s} to a ${singular(resource)}`);
    if (method === "PATCH")
      return mkEp(resource, `update-${s}`, op, method, path, pos, [], true, `Update a ${s} of a ${singular(resource)}`);
    if (method === "DELETE")
      return mkEp(resource, `remove-${s}`, op, method, path, pos, [], false, `Remove a ${s} from a ${singular(resource)}`);
  }

  console.warn(`skipped unhandled ${method} ${path}`);
  return null;
}

function mkEp(resource, command, op, method, path, positionals, query, hasBody, fallbackDesc) {
  return {
    resource,
    command,
    method,
    path,
    description: cleanSummary(op.summary, fallbackDesc),
    positionals,
    query,
    hasBody,
  };
}

function isParam(seg) {
  return seg.startsWith("{") && seg.endsWith("}");
}
function paramName(seg) {
  return seg.slice(1, -1);
}
function singular(s) {
  if (s.endsWith("ies")) return s.slice(0, -3) + "y";
  if (s.endsWith("sses")) return s.slice(0, -2);
  if (s.endsWith("s") && !s.endsWith("ss")) return s.slice(0, -1);
  return s;
}
function cleanSummary(s, fallback) {
  const v = (s ?? "").toString().trim();
  const picked = v || fallback;
  return picked
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\r?\n/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}
function toCamel(s) {
  return s.replace(/-([a-z0-9])/g, (_, c) => c.toUpperCase());
}
function toKebab(s) {
  return s.replace(/_/g, "-");
}

// ---------- emission ----------

function emitResourceFile(resource, endpoints) {
  endpoints.sort((a, b) => a.command.localeCompare(b.command));
  const varName = (cmd) => "cmd_" + cmd.replace(/-/g, "_");
  const body = endpoints.map((ep) => emitCommand(ep, varName(ep.command))).join("\n\n");
  const subCommands = endpoints
    .map((ep) => `    "${ep.command}": ${varName(ep.command)},`)
    .join("\n");
  const rootName = toCamel(resource) + "Command";

  return (
    `// AUTO-GENERATED by scripts/generate-commands.mjs - DO NOT EDIT\n` +
    `import { defineCommand } from "citty";\n` +
    `import type { TimrClient } from "timr-sdk";\n` +
    `import { globalArgs, printJson, readBody, resolveClient, numOrUndef, csv } from "../_shared.js";\n\n` +
    body +
    `\n\nexport const ${rootName} = defineCommand({\n` +
    `  meta: { name: "${resource}", description: "Manage ${resource}" },\n` +
    `  subCommands: {\n${subCommands}\n  },\n` +
    `});\n`
  );
}

function emitCommand(ep, varName) {
  const argLines = [];
  argLines.push(`    ...globalArgs,`);
  for (const p of ep.positionals) {
    argLines.push(
      `    "${toKebab(p.name)}": { type: "positional", description: "${cleanSummary(p.description, p.name)}", required: true },`,
    );
  }
  const queryFlags = [];
  for (const q of ep.query) {
    if (q.in !== "query") continue;
    const name = toKebab(q.name);
    const type = cittyType(q.schema ?? {});
    const desc = cleanSummary(q.description, q.name);
    argLines.push(`    "${name}": { type: "${type}", description: "${desc}" },`);
    queryFlags.push({ raw: q.name, flag: name, schema: q.schema ?? {} });
  }
  if (ep.hasBody) {
    argLines.push(
      `    data: { type: "string", description: "Request body as JSON string, @file.json, or '-' for stdin" },`,
    );
  }

  const paramsParts = [];
  if (ep.positionals.length) {
    const pathFields = ep.positionals
      .map((p) => `"${p.name}": args["${toKebab(p.name)}"] as string`)
      .join(", ");
    paramsParts.push(`path: { ${pathFields} }`);
  }
  if (queryFlags.length) {
    const queryFields = queryFlags
      .map((q) => `"${q.raw}": ${coerce(q)}`)
      .join(", ");
    paramsParts.push(`query: { ${queryFields} }`);
  }

  const callOptsParts = [];
  if (paramsParts.length) callOptsParts.push(`params: { ${paramsParts.join(", ")} }`);
  if (ep.hasBody) callOptsParts.push(`body: (await readBody(args.data)) as never`);
  const callOpts = callOptsParts.length ? `{ ${callOptsParts.join(", ")} }` : "{}";

  const clientCall =
    `(client as TimrClient).${ep.method}("${ep.path}" as never, ${callOpts} as never)`;

  return (
    `const ${varName} = defineCommand({\n` +
    `  meta: { name: "${ep.command}", description: "${ep.description}" },\n` +
    `  args: {\n${argLines.join("\n")}\n  },\n` +
    `  async run({ args }) {\n` +
    `    const client = await resolveClient(args);\n` +
    `    const { data } = await ${clientCall};\n` +
    `    printJson(data);\n` +
    `  },\n` +
    `});`
  );
}

function cittyType(schema) {
  const t = schema.type;
  if (t === "boolean") return "boolean";
  return "string";
}

function coerce(q) {
  const flag = `args["${q.flag}"]`;
  const t = q.schema.type;
  if (t === "integer" || t === "number") return `numOrUndef(${flag})`;
  if (t === "array") return `csv(${flag})`;
  if (t === "boolean") return `${flag} as boolean | undefined`;
  return `${flag} as string | undefined`;
}
